<h3>ПРОГРАММА МЕРОПРИЯТИЯ</h3>

<p><b>4 октября (вторник), 2016г.<br>
The Ritz-Carlton, Moscow<br />
10:00 – 21:30</b></p>

<p>Благодарим  вас за интерес, проявленный к посещению утренней сессии выставки Luxury Travel Mart Москва, которая проводится в закрытом режиме и для ограниченного количества гостей.</p>

<p>Пожалуйста, ознакомьтесь с детальной программой мероприятия:</p>

<p><b><i>Место проведения:</i></b></p>

<p>Отель The Ritz-Carlton, Moscow (Тверская улица, 3.  Метро: Театральная, Охотный Ряд, Площадь Революции). </p>

<p>Гардероб для гостей располагается на втором этаже, у лестницы. </p>

<p>Гости могут воспользоваться услугой платной парковки в отеле The Ritz-Carlton, Moscow.</p>


<p><b><i>Регистрация гостей:</i></b></p>

<p>Начало в <b>09:30.</b> При входе в зал "Ballroom" будут находиться стойки регистрации  "Зарегистрированные гости". Для входа в залы необходимо предъявить именной бейдж, который зарегистрированные гости смогут получить на стойках регистрации.</p>

<p>Все гости мероприятия на стойках регистрации смогут также получить план-схему расположения рабочих мест участников на утренней сессии.</p>

<p><b><i>Программа:</i></b></p>

<p><b>10:00 – 14:20</b> Рабочие встречи согласно предварительно составленному на сайте расписанию. Каждая встреча продолжительностью 10 минут, 5 минут на переход от участника к участнику. Начало и окончание встречи – специальное звуковое оповещение.</p>

<p>Просим  вас строго придерживаться расписания встреч и обязательно посещать все запланированные встречи.</p>
<p>Залы "Ballroom", "Moscow" - второй (конференц) этаж отеля, доступ на этаж по парадной лестнице. </p>

<p><b>11:55 – 12:10</b> Перерыв на кофе. Фойе зала "Ballroom".</p>

<p><b>14:30 – 15:30</b> Ланч для участников и гостей утренней сессии. Возможность для неформального общения.<br />
Фойе зала "Ballroom".</p>

<p><b>15:30 – 17:30</b> Рабочие встречи без расписаний с туристическими компаниями из регионов России и стран СНГ. Каждая встреча продолжительностью 5 минут. Начало и окончание встречи – специальное звуковое оповещение. Участие в данной сессии возможно только для компаний, посещающих LTM по программе Hosted Buyer.</p>

<p><b>19:00 – 21:30</b> Вечерний коктейль в непринуждённой атмосфере, без заранее назначенных встреч. Зал "Ballroom".

</p>

<p>Курение в залах не разрешается. </p>


<p><b>Рабочий язык мероприятия – английский.</b> Просим иметь при себе достаточное количество визитных карточек на английском языке. Посетители без визитных карточек на мероприятие допускаться не будут. </p>



